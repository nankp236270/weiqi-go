echo ""

# 专题文档
echo "专题文档:"
[ -f "日志系统说明.md" ] && mv "日志系统说明.md" "10-日志系统说明.md" && echo "✓ 日志系统说明.md → 10-日志系统说明.md"
echo ""

echo "✅ 重命名完成！"
echo ""
echo "查看重命名后的文档列表:"
ls -1 *.md | grep -E "^[0-9]" | sort

