[ -f "项目最终总结.md" ] && mv "项目最终总结.md" "06-开发总结-阶段4.md" && echo "✓ 项目最终总结.md → 06-开发总结-阶段4.md"
echo ""

# 最终交付文档
echo "最终交付文档:"
